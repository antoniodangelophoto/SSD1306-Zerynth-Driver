    def getCarattere(caracter):
        """Funzione Getter che cerca nel dizionario e restituisce la lista con gli array di 8bit """
        return CARATTERI.get(caracter)


    def displayPrint(arrayPixel):
        """Funzione Stampa un array verticale di 8bit"""
        #print("arraypixel: ",arrayPixel)
        self.write(bytearray([0x40,arrayPixel]))


    def contacarattere(car):
        """Conto gli spazi in orizzontale che occupano i caratteri
            prende in input un carattere(key)"""
        carattere=getCarattere(car)
        lenght=len(carattere)
        #print("lenghta: ",lenght)
        return lenght


    def stampacarattere(c):
        """Funzione che stampa i caratteri """
        carattere=getCarattere(c)
        global cont
        cont=cont+contacarattere(c)
        #print("cont stampacarattere: ",cont)
        for y in range(0,len(carattere)):
            displayPrint(carattere[y])
            

    # ->VAI A CAPO
    def vaiacapo(spaziodisp):
        """Se il carattere supera la risoluzione del display, vado a capo"""

        #global spaziodisponibile
        for i in range(spaziodisp):
            displayPrint(0x00) 


    # ->STAMPA STRINGA
    def stampastringa(stringa):
        """Stampo la stringa sul display"""

        global cont
        global spaziodisponibile
        cont=0
        spaziodisponibile=128
        
        for carattere in stringa:
            carattere=carattere.upper()
            if(spaziodisponibile>contacarattere(carattere)):
                stampacarattere(carattere)
            else:
                vaiacapo(spaziodisponibile)
                cont=0
                spaziodisponibile=128
                stampacarattere(carattere)
            spaziodisponibile=128-cont
        vaiacapo(spaziodisponibile)
        #print(cont)


    # ->STAMPA BITMAP
    def stampaBitmap(bitmap):
        """Tale funzione prende una lista in ingresso come parametro e ne stampa l'intero contenuto"""
        lenght=len(bitmap)
        #print("length: ",lenght)
        for i in range(0,lenght):
            displayPrint(bitmap[i])
        

    # ->FUSIONE 2 BITMAP
    def fusioneBitmap(lista1, lista2):
        """Funzione prende in ingresso 2 liste di bitmap e le unisce in un unica lista bitmap"""

        lenght1=len(lista1)
        #print("lenght1: ",lenght1)
        lenght2=len(lista2)
        #print("lenght2: ",lenght2)

        if(lenght1<1024):
            for x in range(lenght1,1024):
                lista1.insert(x, 0x00)

        if(lenght2<1024):
            for y in range(lenght2,1024):
                lista2.insert(y, 0x00)
        
        lista3=[]

        for z in range(0,1024):
            val=lista1[z] | lista2[z]
            lista3.insert(z, val)
        return lista3
        

    def aggiugiRigaBitmap(n):
        lista=[]
        for x in range(0,n):
            for y in range(0,128):
                lista.append(0x00)
        return lista

    # ->CONVERTITORE STRINGA A LISTA BITMAP
    def stringToBitmap(stringa, riga, orientamento):
        """Funzione converte stringa di caratteri in una lista bitmap
            ->riga= 0,1,2,3,4,5,6,7
            ->orientamento= 0(Sx), 1(Central), 2(Dx)
        """
        bitmap=[]
        bitmapFinal=[]

        if((riga>0) and (riga<8)):
            bitmapFinal=aggiugiRigaBitmap(riga)
        
        for carattere in stringa:
            car=getCarattere(carattere)
            lenghtCar=len(car)
            if(lenghtCar>1):
                for x in range(0,lenghtCar):
                    bitmap.append(car[x])

        lenghtBitmap=len(bitmap)

        
        if(orientamento==1):
            if(lenghtBitmap<=128):    
                for i in range(0, 64-int(lenghtBitmap/2)):
                    bitmapFinal.append(0x00)
                for x in range(0,lenghtBitmap):
                    bitmapFinal.append(bitmap[x])
                    
        
        elif(orientamento==2):
            if(lenghtBitmap<=128):
                for i in range(0,128-lenghtBitmap):
                    bitmapFinal.append(0x00)
                for x in range(0,lenghtBitmap):
                        bitmapFinal.append(bitmap[x])
        
        else: #orientamento ==1 or altri
            for x in range(lenghtBitmap):
                bitmapFinal.append(bitmap[x])
        
        lenghtBitmapFinal=len(bitmapFinal)

        if(lenghtBitmapFinal<1024):
            for y in range(lenghtBitmap,1024):
                bitmapFinal.append(0x00)

        return bitmapFinal
        

